#include <stdio.h>
#include <stdlib.h>
/**
 * add- Function adds two ints
 * @a: First int
 * @b: Send int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

#include <stdio.h>
#include <stdlib.h>
/**
 * sub - Function substracts two ints
 * @a: First int
 * @b: Send int
 * Return: substraction
 */
int sub(int a, int b)
{
	return (a - b);
}

#include <stdio.h>
#include <stdlib.h>
/**
 * mod - Function returns modulus two ints
 * @a: First int
 * @b: Send int
 * Return: modulus
 */
int mod(int a, int b)
{
	return (a % b);
}

#include <stdio.h>
#include <stdlib.h>
/**
 * mul - Function returns product two ints
 * @a: First int
 * @b: Send int
 * Return: mul
 */
int mul(int a, int b)
{
	return (a * b);
}
